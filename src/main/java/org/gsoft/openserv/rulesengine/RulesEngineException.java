package org.gsoft.openserv.rulesengine;

public class RulesEngineException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5988153858021070048L;

	public RulesEngineException() {
		super();
	}

	public RulesEngineException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RulesEngineException(String arg0) {
		super(arg0);
	}

	public RulesEngineException(Throwable arg0) {
		super(arg0);
	}

	
}
